function changeLang(to) {
    if (!to) {
        to = (lang === ru) ? "en" : "ru";
    }

    switch (to) {
        case "ru":
            lang = ru;
            break;

        case "en":
            lang = en;
            break;
    }

    for (const name of langDependent) {
        try {
            el(name).innerHTML = lang[name];
        }
        catch (e) {

        }
    }

    const saveFileNameInput = el("saveFileName");
    if (saveFileNameInput) saveFileNameInput.placeholder = lang.saveFileNamePlaceholder;

    const refUrlInput = el("refUrl");
    if (refUrlInput) refUrlInput.placeholder = lang.refUrlPlaceholder;

    showInfo(selectedId);

    if (typeof saveAllSettings === 'function') {
        saveAllSettings();
    }
}

